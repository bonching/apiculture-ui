// Generate mock history data
const generateHistory = (baseValue: number, variance: number, points: number = 24) => {
  return Array.from({ length: points }, (_, i) => ({
    time: `${23 - i}h`,
    value: Math.round((baseValue + (Math.random() - 0.5) * variance) * 10) / 10,
  })).reverse();
};

